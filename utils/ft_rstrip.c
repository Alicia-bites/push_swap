#include "../headers/ft_push_swap.h"

void    ft_rstrip(char *s)
{
    int    i;

    i = ft_strlen(s) - 1;
    while (i >= 0 && (ft_isspace(s[i])))
        i--;
    s[i + 1] = '\0';
}